export interface WindowStyles {
    /**
     * Transformacja CSS do pozycjonowania okna
     */
    transform: string;

    /**
     * Szerokość okna w pikselach
     */
    width: string;

    /**
     * Wysokość okna w pikselach
     */
    height: string;

    /**
     * Indeks Z do określenia kolejności wyświetlania okien
     */
    zIndex: number;
}
